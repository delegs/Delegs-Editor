package de.signWritingEditor.shared.exceptions;

public class DirectoryNotFoundException extends Exception {

	public DirectoryNotFoundException() {
		
	}

	public DirectoryNotFoundException(String arg0) {
		super(arg0);
		
	}

	public DirectoryNotFoundException(Throwable arg0) {
		super(arg0);
		
	}

	public DirectoryNotFoundException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DirectoryNotFoundException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
