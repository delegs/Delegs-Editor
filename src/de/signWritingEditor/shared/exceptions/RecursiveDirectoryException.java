package de.signWritingEditor.shared.exceptions;

public class RecursiveDirectoryException extends Exception {

	public RecursiveDirectoryException() {
		
	}

	public RecursiveDirectoryException(String arg0) {
		super(arg0);
		
	}

	public RecursiveDirectoryException(Throwable arg0) {
		super(arg0);
		
	}

	public RecursiveDirectoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public RecursiveDirectoryException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
