	public String convertBswToIswa(int baseSymbol, int fill, int rotation) {
		assert baseSymbol == 0 || baseSymbol == 1 || baseSymbol == 2 || baseSymbol == 3 // Specials
				|| (baseSymbol >= 0x100 && baseSymbol <= 0x38B) // ISWA Symbols
				|| (baseSymbol >= 0x3E8 && baseSymbol <= 0x3EC) // Non ISWA
				|| (baseSymbol >= 0x44C && baseSymbol <= 0x469) // FingerAlphabet
		: "Precondition failed: baseSymbol == 0 ||Â baseSymbol == 1 || baseSymbol == 3 || baseSymbol >= 0x100 && baseSymbol <= 0x38B || (baseSymbol >= 0x3E8 && baseSymbol <= 0x3EC || (baseSymbol >= 0x44C && baseSymbol <= 0x469";

		assert fill >= 0 : "Precondition failed: fill >= 0";
		assert fill < 6 : "Precondition failed: fill < 6";
		assert rotation >= 0 : "Precondition failed: rotation >= 0";
		assert rotation < 16 : "Precondition failed: rotation < 16";

		String fillString = "0" + (fill + 1);
		int rotationPlusOne = rotation + 1;
		String rotationString = rotationPlusOne > 9 ? String.valueOf(rotationPlusOne) : "0" + rotationPlusOne;

		String iswaId = bswToIswa2010Map.get(baseSymbol) + "-" + fillString + "-" + rotationString;

		assert SymbolFactory.isValidIswaId(iswaId) : "Postcondition failed: SymbolFactory.isValidIswaId(result)";
		return iswaId;
	}

	public String convertIswaToBsw(String iswaCode) {
		assert iswaCode != null : "Precondition failed: iswaCode != null";
		assert SymbolFactory.isValidIswaId(iswaCode) : "Precondition failed: SymbolFactory.isValidIswaId(iswaCode)";

		StringBuilder stringBuilder = new StringBuilder();

		String symbol = iswaCode.substring(0, 12);
		int bswSymbol = iswa2010ToBswMap.get(symbol);

		stringBuilder.append(Integer.toHexString(bswSymbol));

		// Bsw code must have 3 digits
		while (stringBuilder.length() < 3) {
			stringBuilder.insert(0, "0");
		}

		String[] fillAndRotation = iswaCode.substring(13).split("-");
		stringBuilder.append(Integer.toHexString(Integer.parseInt(fillAndRotation[0]) - 1));
		stringBuilder.append(Integer.toHexString(Integer.parseInt(fillAndRotation[1]) - 1));

		return stringBuilder.toString();
	}

	private Map<String, Integer> createIswa2010ToBswMap(Map<Integer, String> bswToIswaMap) {
		assert bswToIswaMap != null : "Precondition failed: bswToIswaMap != null";

		Map<String, Integer> result = new HashMap<String, Integer>(bswToIswaMap.size());

		for (Map.Entry<Integer, String> mapEntry : bswToIswaMap.entrySet()) {
			result.put(mapEntry.getValue(), mapEntry.getKey());
		}

		return result;
	}

